public class Deco {
    String label;
    String serieDeco;
    String serieTarjeta;

    public Deco(){}

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getSerieDeco() {
        return serieDeco;
    }

    public void setSerieDeco(String serieDeco) {
        this.serieDeco = serieDeco;
    }

    public String getSerieTarjeta() {
        return serieTarjeta;
    }

    public void setSerieTarjeta(String serieTarjeta) {
        this.serieTarjeta = serieTarjeta;
    }
}
